package util;

public class Stats {

    public int health;
    public int attack;
    public int defense;
    public int luck;
    public int range;
    public int lineOfSight;

    public Stats(int health, int attack, int defense, int luck, int range, int lineOfSight) {
        this.health = health;
        this.attack = attack;
        this.defense = defense;
        this.luck = luck;
        this.range = range;
        this.lineOfSight = lineOfSight;
    }
}